    private void GerarRelatorio(int tipo) {
        //Tipo=1 prontuario Não devolvido
        //Tipo=2 prontuario Reservado
        //Tipo=3 prontuario Emprestado
    sctp2.BancodeDados.conexao con= new sctp2.BancodeDados.conexao();
            HashMap filtro= new HashMap();
            filtro.put("", null);
            String path= null;
        try {
        switch (tipo) {
            case 1:
                path= new File("E:\\NetBeansProjects\\SCTP2\\build\\classes\\sctp2\\relatorios\\jasper/ProntuarioAtrasados.jasper").getCanonicalPath();
                break;
            case 2:
                path= new File("E:\\NetBeansProjects\\SCTP2\\build\\classes\\sctp2\\relatorios\\jasper/ProntuariosReservados.jasper").getCanonicalPath();
                break;
            case 3:
                path= new File("E:\\NetBeansProjects\\SCTP2\\build\\classes\\sctp2\\relatorios\\jasper/ProntuáriosEmprestados.jasper").getCanonicalPath();
                break;
            default:
                break;
        }
            System.out.println("caminho do jasper: "+path);
            JasperPrint print= JasperFillManager.fillReport(path,null,con.getConnection());
            JasperViewer viewer = new JasperViewer(print,true);
            viewer.setVisible(true);
        } catch (IOException ex) {
            Logger.getLogger(Prontuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JRException ex) {
            Logger.getLogger(Prontuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Prontuarios.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Prontuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
