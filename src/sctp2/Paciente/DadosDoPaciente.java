    private void jRetiraListaNegraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRetiraListaNegraActionPerformed
        if (pacientelistaNegra == 1) {
            int resposta = JOptionPane.showConfirmDialog(null, "Tem Certeza que deseja retirar este paciente da lista negra?");
            if (resposta == 0) {
                sctp2.BancodeDados.conexao acesso = new sctp2.BancodeDados.conexao();
                boolean retorno;
                try {
                    retorno = acesso.MudaStatusListaNegraPaciente(rgPaciente);
                    if (retorno == true) {
                        SetValoresCampos(rgPaciente);
                        jNotificao.setText("O paciente foi retirado da lista Negra");
                        pacientelistaNegra = 0;
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(DadosDoPaciente.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(DadosDoPaciente.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }//fim do if paciente lista negra==1
        else if (pacientelistaNegra == 0) {
            new sctp2.Paciente.ColocarnaListaNegra(jNome.getText(), codigopaciente, rgPaciente).setVisible(true);
            this.setVisible(false);
            pacientelistaNegra = 1;
        }
    }//GEN-LAST:event_jRetiraListaNegraActionPerformed

