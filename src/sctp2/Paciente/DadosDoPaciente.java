    private void SetValoresCampos(String rg) throws ClassNotFoundException {
        //Esta função recebe os dados do banco de dados e carrega tudo nos campos da interface 
        sctp2.BancodeDados.conexao acesso = new sctp2.BancodeDados.conexao();
        ArrayList<Pesquisar> ListarPesquisa = new ArrayList<Pesquisar>();
        ListarPesquisa = acesso.DetalhesdoPaciente(rg);
        codigopaciente = ListarPesquisa.get(0).getCodigo();
        jNome.setText(ListarPesquisa.get(0).getNome());
        jocupação.setText(ListarPesquisa.get(0).getOcupação());
        jpai.setText(ListarPesquisa.get(0).getPai());
        jMae.setText(ListarPesquisa.get(0).getMae());
        if (ListarPesquisa.get(0).getSexo() == 0) {
            jsexo.setSelectedIndex(1);//Masculino
        }
        if (ListarPesquisa.get(0).getSexo() == 1) {
            jsexo.setSelectedIndex(2);//Feminino
        }

        jrg.setText(ListarPesquisa.get(0).getRg());
        if (ListarPesquisa.get(0).getProcedencia() == 0) {
            jprocedencia.setSelectedIndex(0);
        }
        if (ListarPesquisa.get(0).getProcedencia() == 1) {
            //jprocedencia.setToolTipText("Zona Rural");
            jprocedencia.setSelectedIndex(1);
        }
        if (ListarPesquisa.get(0).getEstadoCivil() == 0) {
            jestadocivil.setSelectedIndex(0);//solteiro
        }
        if (ListarPesquisa.get(0).getEstadoCivil() == 1) {
            jestadocivil.setSelectedIndex(1);//casado
        }
        if (ListarPesquisa.get(0).getEstadoCivil() == 2) {
            jestadocivil.setSelectedIndex(2);//viuvo
        }
        if (ListarPesquisa.get(0).getEstadoCivil() == 3) {
            jestadocivil.setSelectedIndex(3);//Divorciado
        }
        jconjuge.setText(ListarPesquisa.get(0).getConjugue());
        jrua.setText(ListarPesquisa.get(0).getRua());
        jbairro.setText(ListarPesquisa.get(0).getBairro());
        jcidade.setText(ListarPesquisa.get(0).getCidade());
        jnumeroDoEndereco.setText(ListarPesquisa.get(0).getNumeroCasa());
        jcep.setText(ListarPesquisa.get(0).getCep());
        jtelefone.setText(ListarPesquisa.get(0).getTelefone());
        jTelefoneFixo.setText(ListarPesquisa.get(0).getTelefonefixo());
        jlistaNegraJustificativa.setText(ListarPesquisa.get(0).getMotivoListaNegra());
        if (ListarPesquisa.get(0).getPacienteAlta() == 0) {
//            jCPacieteComAlta.setSelected(false);
        }
        if (ListarPesquisa.get(0).getPacienteAlta() == 1) {
            //          jCPacieteComAlta.setSelected(true);
        }
        jnaturalidade.setText(ListarPesquisa.get(0).getNaturalidade());
        jnascimento.setText(ListarPesquisa.get(0).getDataNascimento());
        pacientelistaNegra = ListarPesquisa.get(0).getListaNegra();
        if (pacientelistaNegra == 1) {
            jNotificao.setText("Atenção! Paciente da Lista Negra.");
        }
        statusdotratamento = ListarPesquisa.get(0).getPacienteAlta();//se o valor for 0 (zero)significa que esta em tratamento
        if (statusdotratamento == 0) {
            jTratamento.setText("Em tratamento");
            jNovoTratamento.setEnabled(true);
        } else if (statusdotratamento == 1) {
            jTratamento.setText("Paciente com Alta");
            jButton1.setEnabled(false);
            jNovoTratamento.setVisible(true);
            jNovoTratamento.setEnabled(true);
        }
        if (pacientelistaNegra == 1) {
            jNotificao.setText("Atenção: Paciente da lista negra.");
            jRetiraListaNegra.setVisible(true);
        } else if (pacientelistaNegra == 0) {
            jRetiraListaNegra.setVisible(true);
            jRetiraListaNegra.setText("Colocar na lista Negra");
        }

    }

    private boolean VerificaCampos() {
        boolean retorno = true;
        if (jNome.getText().trim().equals("")) {
            retorno = false;
            jNome.setBorder(new LineBorder(Color.red));
        }
        if (jrg.getText().trim().equals("")) {
            retorno = false;
            jrg.setBorder(new LineBorder(Color.red));
        }
        if (jsexo.getSelectedIndex() == 0) {
            jsexo.setBorder(new LineBorder(Color.red));
            retorno = false;
        }

        return retorno;
    }

