    private void PesquisarProntuario() throws ClassNotFoundException {
        sctp2.BancodeDados.conexao acesso = new sctp2.BancodeDados.conexao();
        ArrayList<PesquisarProntuario> ListarPesquisa;
        ListarPesquisa = acesso.PesquisarProntuario(idProntuario);
        if (ListarPesquisa.isEmpty()) {
            jTNotificacao.setForeground(Color.red);
            jTNotificacao.setText("Algum erro impediu de mostrar os dados do prontuário, tente novamente.");
        } else {
            jProntuario.setText(ListarPesquisa.get(0).getProntuario());
            jPaciente.setText(ListarPesquisa.get(0).getPaciente());
            jLocalizacaoFisica.setText(ListarPesquisa.get(0).getLocalizacaoProntuario());
            jInformacao.setText(ListarPesquisa.get(0).getInformacoes());

            jTNotificacao.setForeground(Color.BLUE);
            jTNotificacao.setText("Clique em salvar para finalizar a edição!");
        }
    }
