    private void jSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSalvarActionPerformed
        conexao conexao = new conexao();
        try {
            boolean retorno = conexao.AtualizaDadosProntuario(jProntuario.getText(), jInformacao.getText(), jLocalizacaoFisica.getText(), idProntuario);
            jTNotificacao.setForeground(Color.blue);
            if (retorno == true) {
                jTNotificacao.setText("Atualizado com sucesso!");
                jProntuario.setEnabled(false);
                jLocalizacaoFisica.setEnabled(false);
                jInformacao.setEnabled(false);
                jSalvar.setEnabled(false);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditarProntuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(EditarProntuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jSalvarActionPerformed

