    private void EfetuaPesquisa() throws ClassNotFoundException {//Esta função faza a pesquisa e popula a tabela com os nomes encontrados
        sctp2.BancodeDados.ConexaoPacotePesquisar acesso = new sctp2.BancodeDados.ConexaoPacotePesquisar();
        ArrayList<Pesquisar> listaPesquisa;//array pra pegar o resultado da pesquisa
        DefaultTableModel valor = (DefaultTableModel) jTable1.getModel();//criando a chave valor para o objeto tabela
        listaPesquisa = acesso.PesquisarResponsavelProntuario(jPesquisa.getText(),1);// o valor 1 define o tipo de pesquisa
        LimpaTela();
        if (listaPesquisa.isEmpty()) {
            jNotificacao.setText("Sem resultados");
        } else {

            for (int i = 0; i < listaPesquisa.size(); i++) {
                System.out.println("::" + listaPesquisa.get(i).getNome());
                valor.addRow(new String[]{Integer.toString(listaPesquisa.get(i).getCodigo()), listaPesquisa.get(i).getNome()});
                BloqueiaCampos(false);//esta função bloqueia os campos para impedir a edição das informações
                jNotificacao.setForeground(Color.blue);
                jNotificacao.setText("Clique no nome para ver seus detalhes!");
            }
        }
    }

    private void LimpaTela() {
        //limpa todos os campos
        jTable1.setSelectionBackground(Color.BLUE);//defini a cor de seleção da tabela para azul
        DefaultTableModel tabela;
        tabela = (DefaultTableModel) jTable1.getModel();
        tabela.setNumRows(0);
        //********************************************
        nomeresponsavel.setText("");
        jFTelefoneCelular.setText("");
        jFTelefoneFixo.setText("");
        jnotificacao2.setText("");
        jnotificacao2.setForeground(Color.blue);
        nomeresponsavel.setBorder(new LineBorder(Color.gray));
        jLabel6.setText("");
        jEditar.disable();
        jExcluir.disable();
        jNovoResponsavel.disable();
        idDoResponsavel = -1;//valor padrão da variavel
        BloqueiaCampos(false);//bloqueia os botoes editar excluir
        nomeresponsavel.setBackground(Color.white);
            jFTelefoneCelular.setBackground(Color.white);
            jFTelefoneFixo.setBackground(Color.white);
            jinformacaodoconteine.setText("");
    }

    private void BloqueiaCampos(boolean status) {
        if (status == false) {
            nomeresponsavel.setEditable(false);
            jFTelefoneCelular.setEditable(false);
            jFTelefoneFixo.setEditable(false);
            nomeresponsavel.setBackground(Color.lightGray);
            jFTelefoneCelular.setBackground(Color.lightGray);
            jFTelefoneFixo.setBackground(Color.lightGray);
            jEditar.enable(false);
            jExcluir.enable(false);
            jNovoResponsavel.enable(false);

            //------------------------------------
        } else
            if (status == true) {
                nomeresponsavel.setEditable(true);
                jFTelefoneCelular.setEditable(true);
                jFTelefoneFixo.setEditable(true);
                nomeresponsavel.setBackground(Color.white);
                jFTelefoneCelular.setBackground(Color.white);
                jFTelefoneFixo.setBackground(Color.white);
               

    
//------------------------------------
    
    
    }
    
    
    }

