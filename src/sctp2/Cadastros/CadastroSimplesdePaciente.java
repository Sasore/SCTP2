    private void SetPassaValoresCadastroSimples() {
        sctp2.ClassesdeControle.CadastroSimplesdePaciente acesso = new sctp2.ClassesdeControle.CadastroSimplesdePaciente();
        acesso.setNomePaciente(jNome.getText());
        acesso.setNascimento(jNascimento.getText());
        acesso.setTelefone(jTelefone.getText());
        acesso.setRg(jRg.getText());
        acesso.setPrincipalQueixa(jPrincipalQueixa.getText());
        acesso.setTratamentoSolicitado(jTratamentoSolicitado.getText());

    }

    private boolean VerificaCampos() {
        boolean retorno = true;
        if (jNome.getText().trim().equals(""));
        {
            jNome.setBorder(new LineBorder(Color.red));
            retorno = false;
        }
        if (jRg.getText().trim().equals(""));
        {
            jRg.setBorder(new LineBorder(Color.red));
            retorno = false;
        }
        if (jPrincipalQueixa.getText().trim().equals(""));
        {
            jPrincipalQueixa.setBorder(new LineBorder(Color.red));
            retorno = false;

        }
        if (jTratamentoSolicitado.getText().trim().equals(""));
        {
            jTratamentoSolicitado.setBorder(new LineBorder(Color.red));
            retorno = false;
        }
        return retorno;
    }
