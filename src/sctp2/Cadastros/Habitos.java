    private void jCfumaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jCfumaItemStateChanged
        if (jCfuma.getSelectedItem() == "Não") {
            jCFumaHaQuantoTempo.setEnabled(false);
            jCFumaHaQuantoTempo.setSelectedIndex(0);
            jCFumoQuantidadePorDia.setEnabled(false);
            jCFumoQuantidadePorDia.setSelectedIndex(0);
            jCTipodeCigarro.setEnabled(false);
            jCTipodeCigarro.setSelectedIndex(0);
        } else if (jCfuma.getSelectedItem() == "Sim") {
            jCFumaHaQuantoTempo.setEnabled(true);
            jCFumoQuantidadePorDia.setEnabled(true);
            jCTipodeCigarro.setEnabled(true);
        }
    }//GEN-LAST:event_jCfumaItemStateChanged

