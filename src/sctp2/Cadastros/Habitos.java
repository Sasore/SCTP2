    private boolean VerificaCampos() {
        boolean retorno = true;
        jTAlerta.setText("");
        if ((jCFumoQuantidadePorDia.getSelectedItem().equals("Selecione")) && (jCfuma.getSelectedItem().equals("Sim"))) {
            jTAlerta.setText("Escolha uma opção para os campos em vermelho ");
            jCFumoQuantidadePorDia.setBackground(Color.red);
            retorno = false;
        }
        if ((jCTipodeCigarro.getSelectedItem().equals("Selecione")) && (jCfuma.getSelectedItem().equals("Sim"))) {
            jTAlerta.setText("Escolha uma opção para os campos em vermelho ");
            jCTipodeCigarro.setBackground(Color.red);
            retorno = false;
        }
        if ((jCFumaHaQuantoTempo.getSelectedItem().equals("Selecione") && (jCfuma.getSelectedItem().equals("Sim")))) {
            jTAlerta.setText("Escolha uma opção para os campos em vermelho ");
            jCFumaHaQuantoTempo.setBackground(Color.red);
            retorno = false;
        }
        //-------------------------------------------Bebida//----------------------------------------------------------------------
        if ((jCBebidaAlcoolicaHaQuantoTempo.getSelectedItem().equals("Selecione")) && (jCBebidaAlcoolica.getSelectedItem().equals("Sim"))) {
            jTAlerta.setText("Escolha uma opção para os campos em vermelho ");
            jCBebidaAlcoolicaHaQuantoTempo.setBackground(Color.red);
            retorno = false;
        }
        if ((jcBebidaAlcoolicaFrequencia.getSelectedItem().equals("Selecione")) && (jCBebidaAlcoolica.getSelectedItem().equals("Sim"))) {
            jTAlerta.setText("Escolha uma opção para os campos em vermelho ");;
            jcBebidaAlcoolicaFrequencia.setBackground(Color.red);
            retorno = false;
        }
        if ((jCbebidaAlcoolicaTipodeBebida.getSelectedItem().equals("Selecione")) && (jCBebidaAlcoolica.getSelectedItem().equals("Sim"))) {
            jTAlerta.setText("Escolha uma opção para os campos em vermelho ");
            jCbebidaAlcoolicaTipodeBebida.setBackground(Color.red);
            retorno = false;
        }
        return retorno;
    }
